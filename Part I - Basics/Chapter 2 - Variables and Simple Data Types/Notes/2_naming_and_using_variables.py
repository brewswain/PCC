"""
    Variables adhere to a few rules and guidelines. Breaking some of these rules will cause errors, while other rules
are just in place to help you write code that has improved readability. the rules are as follows:
"""
# Variable Names can only contain letters, numbers, and underscores.

# They can start with a letter or underscore, but not with a number. For instance, message_1, not 1_message.

# Spaces are not allowed in variable names, but underscores can be used to separate words.

# Avoid using Python keywords and function names as variable names, for example, print.

# Make Variable names short but descriptive. name is better than n, name_length is better than length_of_persons_name, etc.

# Be careful of using the lowercase letter l and the uppercase letter O as they can be mistaken for the numbers 1 and 0.
