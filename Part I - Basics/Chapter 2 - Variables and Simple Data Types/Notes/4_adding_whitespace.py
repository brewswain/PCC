"""
In programming, whitespace refers to any nonprinting character, such as spaces, tabs, and EOL symbols.
(End Of Line)
"""

# Adding A Tab To Your Text

print('\tBeep')
print('\t\tBoop')

# Adding A Newline To Your Text

print('Languages:\nPython\nJavascript\nHTML/CSS')


# Combining Tabs and Newlines

print('Languages:\n\tPython\n\tJavascript\n\tHTML/CSS')
