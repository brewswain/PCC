
# Integers

"""
You can perform basic bath functions on integers in Python. 
In a terminal session, Python simply returns the result of the operation.
Python also supports order of operations, so you can use multiple operations in one expression.
"""
2 + 3
3 - 2
2 * 3
3 / 2
3 ** 2  # Exponent
2 + 3*4
(2 + 3) * 4
4 % 3  # Modulo

